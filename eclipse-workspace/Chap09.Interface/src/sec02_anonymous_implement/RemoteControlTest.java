package sec02_anonymous_implement;

import sec01_Interface_declare.RemoteControl;

public class RemoteControlTest { // 익명 구현 개체(클래스명을 부여하지 않고 인터페이스를 구현하는 객체를 만들 수 있는 방법) 문장 끝 ;

	public static void main(String[] args) {
		RemoteControl rc = new RemoteControl() {

			@Override
			public void turnOn() {
				System.out.println("전원 켜짐");

			}

			@Override
			public void turnOff() {
				System.out.println("전원 꺼짐");

			}

			@Override
			public void setVolume(int volume) {
				if (volume >= RemoteControl.MAX_VOLUME) { // 볼륨값이 10이 넘어갈때, 최대값이 상수 이기 때문에 인터페이스.최댓값 불러온다
					volume = RemoteControl.MAX_VOLUME;
				} else if (volume < RemoteControl.MIN_VOLUME) {
					volume = RemoteControl.MIN_VOLUME;
				} else {
					volume = volume;
				}
				System.out.println("현재 볼륨: " + volume + " 입니다.");
			}

		};

		rc.turnOn();
		rc.setVolume(12);

	}

}
